const narutoCharacterByNumber = new Map(
  narutoCharacters.map((character) => [character.number, character])
);

export function narutoIds(numbers: number[]): string[] {
  return numbers.map((number) => {
    const character = narutoCharacterByNumber.get(number);
    if (!character) throw new Error(`Personnage Naruto introuvable: ${number}`);
    return character.id;
  });
}

export function pickNarutoCharacters(numbers: number[]): NarutoCharacter[] {
  return numbers.map((number) => {
    const character = narutoCharacterByNumber.get(number);
    if (!character) throw new Error(`Personnage Naruto introuvable: ${number}`);
    return character;
  });
}
